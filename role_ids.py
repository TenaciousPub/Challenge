def get_gender_role_id(gender: str) -> int:
    """Get role ID for gender."""
    g = (gender or "").strip().lower()
    if g == "male":
        return ROLE_MALE_GROUP
    elif g == "female":
        return ROLE_FEMALE_GROUP
    return None
